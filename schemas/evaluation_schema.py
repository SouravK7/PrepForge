"""
Evaluation schemas.

Output contracts from the evaluation ensemble.
Every score must have a reason. No mysterious numbers.
"""

from __future__ import annotations

from datetime import datetime
from enum import Enum

from pydantic import BaseModel, ConfigDict, Field


class GradeEnum(str, Enum):
    """Letter grade mapped from weighted final score."""

    A = "A"
    B = "B"
    C = "C"
    D = "D"
    F = "F"


class ReadinessEnum(str, Enum):
    """Interview readiness level."""

    EXCELLENT = "excellent"
    GOOD = "good"
    AVERAGE = "average"
    POOR = "poor"


class EvaluationScores(BaseModel):
    """
    All dimensional scores from the evaluation ensemble.

    Each score is independently generated by its dedicated evaluator.
    The weighted_final is computed by ScoreFusion using config weights.
    """

    model_config = ConfigDict(frozen=True)

    semantic: float = Field(
        ...,
        ge=0.0,
        le=100.0,
        description="Semantic similarity to sample answer 0-100",
    )
    concept: float = Field(
        ...,
        ge=0.0,
        le=100.0,
        description="Required concept coverage score 0-100",
    )
    communication: float = Field(
        ...,
        ge=0.0,
        le=100.0,
        description="Grammar clarity and structure score 0-100",
    )
    evidence: float = Field(
        ...,
        ge=0.0,
        le=100.0,
        description="Real-world examples and evidence score 0-100",
    )
    reasoning: float = Field(
        ...,
        ge=0.0,
        le=100.0,
        description="Logical depth and argument coherence score 0-100",
    )
    weighted_final: float = Field(
        ...,
        ge=0.0,
        le=100.0,
        description="Weighted combination of all scores 0-100",
    )


class EvaluationEvidence(BaseModel):
    """
    Observable evidence extracted from the answer.

    Makes scoring auditable. Every score references actual answer content.
    """

    model_config = ConfigDict(frozen=True)

    matched_concepts: list[str] = Field(
        default_factory=list,
        description="Required concepts found in the answer",
    )
    missing_concepts: list[str] = Field(
        default_factory=list,
        description="Required concepts absent from the answer",
    )
    optional_concepts_found: list[str] = Field(
        default_factory=list,
        description="Bonus concepts the user mentioned",
    )
    detected_examples: list[str] = Field(
        default_factory=list,
        description="Real-world examples detected in the answer",
    )
    grammar_issues: list[str] = Field(
        default_factory=list,
        description="Specific grammar issues detected",
    )
    strengths: list[str] = Field(
        default_factory=list,
        description="Specific strengths observed in the answer",
    )
    weaknesses: list[str] = Field(
        default_factory=list,
        description="Specific weaknesses observed in the answer",
    )


class EvaluationExplanation(BaseModel):
    """
    Human-readable explanations for each score dimension.

    Every score must be explainable. This schema enforces that.
    Shown directly to the user in the feedback page.
    """

    model_config = ConfigDict(frozen=True)

    semantic_reason: str = Field(
        ...,
        description="Why the semantic score is what it is",
    )
    concept_reason: str = Field(
        ...,
        description="Which concepts were found and which were missing",
    )
    communication_reason: str = Field(
        ...,
        description="Assessment of clarity grammar and structure",
    )
    evidence_reason: str = Field(
        ...,
        description="Whether real-world examples were provided",
    )
    reasoning_reason: str = Field(
        ...,
        description="Assessment of logical depth and argument quality",
    )
    overall_summary: str = Field(
        ...,
        description="One paragraph summary of the entire answer quality",
    )
    improvement_tip: str = Field(
        ...,
        description="Single most impactful improvement the user can make",
    )


class EvaluationOutput(BaseModel):
    """
    Complete output from the evaluation ensemble for one answer.

    This is the primary output of the EvaluationOrchestrator.
    Contains all scores, evidence, explanations, and competency updates.
    Stored in the evaluations database table.
    """

    model_config = ConfigDict(frozen=False)

    id: str = Field(..., description="Unique evaluation identifier")
    session_id: int = Field(..., description="Session this evaluation belongs to")
    answer_id: int = Field(..., description="Answer that was evaluated")
    user_id: int = Field(..., description="User who submitted the answer")
    competency_id: str = Field(..., description="Competency that was assessed")
    question_id: str = Field(..., description="Question that was answered")

    scores: EvaluationScores = Field(
        ...,
        description="All dimensional scores and weighted final",
    )
    grade: GradeEnum = Field(..., description="Letter grade A through F")
    readiness_level: ReadinessEnum = Field(
        ...,
        description="Qualitative readiness assessment",
    )

    evidence: EvaluationEvidence = Field(
        ...,
        description="Observable evidence extracted from the answer",
    )
    explanation: EvaluationExplanation = Field(
        ...,
        description="Human-readable explanation for every score",
    )

    competency_delta: float = Field(
        ...,
        description="Change applied to competency confidence score",
    )
    evaluated_at: datetime = Field(
        default_factory=datetime.utcnow,
        description="When evaluation was completed",
    )
